function getZodiacSign(year) {
  const baseYear = 1900
  const offset = (year - baseYear) % 12
  const adjustedOffset = offset >= 0 ? offset : offset + 12
  return zodiacSigns[adjustedOffset]
}

function getFiveElements(year, month, day) {
  const yearStems = ['甲', '乙', '丙', '丁', '戊', '己', '庚', '辛', '壬', '癸']
  const yearBranches = ['子', '丑', '寅', '卯', '辰', '巳', '午', '未', '申', '酉', '戌', '亥']
  
  const yearIndex = (year - 1984) % 60
  const stemIndex = yearIndex % 10
  const branchIndex = yearIndex % 12
  
  return {
    yearStem: yearStems[stemIndex >= 0 ? stemIndex : stemIndex + 10],
    yearBranch: yearBranches[branchIndex >= 0 ? branchIndex : branchIndex + 12],
    month: month,
    day: day
  }
}

